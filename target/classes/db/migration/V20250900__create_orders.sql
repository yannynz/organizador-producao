CREATE TABLE IF NOT EXISTS orders (
    id                      BIGSERIAL PRIMARY KEY,
    nr                      VARCHAR(255),
    cliente                 VARCHAR(255),
    prioridade              VARCHAR(50),
    data_h                  TIMESTAMPTZ,
    status                  INT,
    data_entrega            TIMESTAMPTZ,
    entregador              VARCHAR(255),
    observacao              TEXT,
    veiculo                 VARCHAR(255),
    data_h_retorno          TIMESTAMPTZ,
    recebedor               VARCHAR(255),
    montador                VARCHAR(255),
    data_montagem           TIMESTAMPTZ,
    emborrachador           VARCHAR(255),
    data_emborrachamento    TIMESTAMPTZ,
    emborrachada            BOOLEAN NOT NULL DEFAULT false,
    data_cortada            TIMESTAMPTZ,
    data_tirada             TIMESTAMPTZ,
    destacador              VARCHAR(10),
    modalidade_entrega      VARCHAR(50),
    data_requerida_entrega  TIMESTAMPTZ,
    usuario_importacao      VARCHAR(255),
    pertinax                BOOLEAN NOT NULL DEFAULT false,
    poliester               BOOLEAN NOT NULL DEFAULT false,
    papel_calibrado         BOOLEAN NOT NULL DEFAULT false
);
